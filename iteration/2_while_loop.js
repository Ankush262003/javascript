//  => While LOOP :
console.log(" => While LOOP :");

let index = 0
while (index <= 10) {
    console.log(`Value of Index is ${index}`)
    index += 2
}




// While Loop on Array:
console.log(" => While Loop on Array:")

let name = ["shivank", "Prashant", "Shivam", "Satyam"]

let len = name.length
let arr = 0
while (arr < len) {
    console.log(`Value is ${name[arr]}`)
    arr = arr + 1
}